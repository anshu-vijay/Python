a=15
print("decimal to binary")
print(bin(a))

print("binary to decimal")
convert_a = bin(a)
print(int(convert_a,2))


b=55
print("decimal to octal")
print(oct(b))

print("octal to decimal")
convert_b = oct(b)
print(int(convert_b,8))


c=79
print("decimal to hexa")
print(hex(c))

print("hexa to decimal")
convert_c = hex(c)
print(int(convert_c,16))